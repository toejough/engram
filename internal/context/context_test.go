// jsonlUserBlockLine builds a JSONL user line with a single text block in an array.
func jsonlUserBlockLine(text string) string {
	type block struct {
		Type string `json:"type"`
		Text string `json:"text"`
	}

	blocks := []block{{Type: "text", Text: text}}

	blocksJSON, err := json.Marshal(blocks)
	if err != nil {
		panic(err)
	}

	return `{"type":"user","message":{"role":"user","content":` + string(blocksJSON) + `}}`
}

// jsonlUserLine builds a JSONL user line with a plain string content field.
func jsonlUserLine(content string) string {
	marshaled, err := json.Marshal(content)
	if err != nil {
		panic(err)
	}

	return `{"type":"user","message":{"role":"user","content":` + string(marshaled) + `}}`
}
