// TestStrip_DropsSkillBodyInjection verifies that USER turns injected by the
// skill harness (starting with "Base directory for this skill:") are dropped.
// Both Strip and ToolSummaryMode paths must filter them.
func TestStrip_DropsSkillBodyInjection(t *testing.T) {
	t.Parallel()

	skillBody := "Base directory for this skill: /Users/joe/.claude/skills/recall\n\n" +
		"# Recall\nThis is the entire SKILL.md content that should be dropped entirely."

	skillLine := jsonlUserBlockLine(skillBody)

	assistantLine := `{"type":"assistant","message":{"role":"assistant",` +
		`"content":[{"type":"text","text":"Searching the vault."}]}}`

	lines := []string{skillLine, assistantLine}

	t.Run("Strip", func(t *testing.T) {
		t.Parallel()

		g := NewGomegaWithT(t)
		result := sessionctx.Strip(lines)

		g.Expect(result).To(HaveLen(1))
		g.Expect(result[0]).To(Equal("ASSISTANT: Searching the vault."))
	})

	t.Run("ToolSummaryMode", func(t *testing.T) {
		t.Parallel()

		g := NewGomegaWithT(t)
		result := sessionctx.StripWithConfig(lines, sessionctx.StripConfig{ToolSummaryMode: true})

		g.Expect(result).To(HaveLen(1))
		g.Expect(result[0]).To(Equal("ASSISTANT: Searching the vault."))
	})
}

