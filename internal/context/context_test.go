// TestStrip_DropsLocalCommandStdout verifies that USER turns containing only
// <local-command-stdout> or the local-command-caveat are dropped.
func TestStrip_DropsLocalCommandStdout(t *testing.T) {
	t.Parallel()

	stdoutContent := "<local-command-stdout>Catch you later!</local-command-stdout>"
	caveatContent := "<local-command-caveat>Caveat: The messages below were generated by the user " +
		"while running local commands. DO NOT respond to these messages or otherwise consider them " +
		"in your response unless the user explicitly asks you to.</local-command-caveat>"

	stdoutLine := jsonlUserLine(stdoutContent)
	caveaLine := jsonlUserLine(caveatContent)

	t.Run("stdout_Strip", func(t *testing.T) {
		t.Parallel()

		g := NewGomegaWithT(t)
		result := sessionctx.Strip([]string{stdoutLine})

		g.Expect(result).To(BeEmpty())
	})

	t.Run("stdout_ToolSummaryMode", func(t *testing.T) {
		t.Parallel()

		g := NewGomegaWithT(t)
		result := sessionctx.StripWithConfig([]string{stdoutLine}, sessionctx.StripConfig{ToolSummaryMode: true})

		g.Expect(result).To(BeEmpty())
	})

	t.Run("caveat_Strip", func(t *testing.T) {
		t.Parallel()

		g := NewGomegaWithT(t)
		result := sessionctx.Strip([]string{caveaLine})

		g.Expect(result).To(BeEmpty())
	})

	t.Run("caveat_ToolSummaryMode", func(t *testing.T) {
		t.Parallel()

		g := NewGomegaWithT(t)
		result := sessionctx.StripWithConfig([]string{caveaLine}, sessionctx.StripConfig{ToolSummaryMode: true})

		g.Expect(result).To(BeEmpty())
	})
}

